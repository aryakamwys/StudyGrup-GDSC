function parentFunction() {  
  const parentVariable = "Ini Variable Induk";  
  
  // ======================================================================================
  // 3. Fungsi Closure
  // ======================================================================================
  function closures() {  
    const closuresVariable = "Ini Local Variables";  
    return closuresVariable + " dan " + parentVariable;  
  }  
  
  return closures();  
}  

